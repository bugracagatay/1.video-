public class while_dongusu_1den10a {
    public static void main(String[] args) {
     //1den 10a yazdırma
        int i=1;
        while (i<10){
            System.out.println(i);
            i++;
        }
        System.out.println("while döngüsü bitti");
        //1den 10a tek sayıları yazdırma
        int x=1;
        while (x<10){
            System.out.println(x);
            x+=2;}
        System.out.println("while döngüsü bitti");
//1den 10a cift sayilari yazdırma
        int z=2;
        while (z<=10){
            System.out.println(z);
            z+=2;}
        System.out.println("while döngüsü bitti");
    }
}
