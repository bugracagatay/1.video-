public class kontol_ifadesi_if {
    public static void main(String args[]) {

        int secenek;
        int a, b;
        secenek = 0;
        a = 5;
        b = 7;
        if (secenek == 0) {
            System.out.println("a+b=" + (a + b));
        }
        if (secenek == 1) {
            System.out.println("a-b=" + (a - b));

        }
        if (secenek == 2) {
            System.out.println("a*b=" + (a * b));

        }
    }
}

